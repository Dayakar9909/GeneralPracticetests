package StringBufferNStringBuilder;

public class ExampleOfStringBuilder {

        public static void main(String[] args) {

            StringBuilder sb = new StringBuilder("Dayakar");

            System.out.println(sb.reverse());
            System.out.println(sb.append(" Reddy"));
            System.out.println(sb.reverse());
            System.out.println(sb.append(" Sama"));
            System.out.println(sb.reverse());

    }
}
